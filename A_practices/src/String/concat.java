package String;
//to add two string


public class concat {

	public static void main(String[] args) {
		
		//using + operator
		
		String s="hai ";
		
		String v="vikram";
		System.out.println(s+v);
		
		
		
		//using concat() method 
		
		String n="bye ";
		String r="vikram";
		System.out.println(n.concat(r));


	}

}
