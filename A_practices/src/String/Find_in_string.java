package String;

public class Find_in_string {

	public static void main(String[] args) {
		
		
		//extract substring for the string using substring(begining index)
		
		String s="vikram";
		System.out.println(s.substring(3));
		
		//extract substring for the string using substrig (begining index , end index)
		
		System.out.println(s.substring(0, 3));
		

	}

}
